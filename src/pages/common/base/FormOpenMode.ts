export enum FormOpenMode {
    /// <summary>
    /// None
    /// </summary>
    None,
    /// <summary>
    /// Add New Mode - When entering new data.
    /// </summary>
    AddNew,
    /// <summary>
    /// Modify Mode - When modifying existing data.
    /// </summary>
    Modify,
    /// <summary>
    /// Detail Mode - When viewing existing data.
    /// </summary>
    Detail,
    /// <summary>
    /// Create Copy Mode - When copying existing data and creating a new data from it.
    /// </summary>
    CreateCopy,
    /// <summary>
    /// Delete Mode - When deleting existing data.
    /// </summary>
    Delete
}